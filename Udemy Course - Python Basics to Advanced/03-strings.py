#Exemplos de strings
#Aspas simples
print('garoto de programa');
#Aspas duplas
print("garotos de programa");
#Caractere de Escape > \ serve para ignorar um caractere na linha de comando
print("garotos de \"programa\"");
#Usar r faz com que o caractere de escape apareça, exemplo:
print(r"garotos de \"programa\"");
#Pode usar aspas duplas dentro de aspas simples e vice-versa
print('garotos de "programa"');
