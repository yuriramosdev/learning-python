# tipo de dado bool (boolean)
# ao questionar algo em um programa só existem duas respostas possíveis: sim (True) ou não (False)
# Existem vários operadores para questionar, dentre eles o ==, que é um operador lógico que questiona se um valor é igual a outro
# Exemplo:

print(10 == 10) #Sim > True 
print(10 == 11) #Não > False
print(type(10 == 11)) #Tipo de dado > bool
print(type(True)) #Tipo de dado > bool
print(type(False)) #Tipo de dado > bool

# Dois caracteres == significa que você questiona
# Um caractere = significa um valor de atribuição