adicao = 10 + 10
print('Adição', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

divisao = 10 / 2.2  #com uma / sempre dá float
print('Divisão', divisao)

divisao_inteira = 10 // 2.2  #com duas // todos os números decimais são ignorados
print('Divisão Inteira', divisao_inteira) 

exponenciacao = 2 ** 10
print('Exponenciação', exponenciacao)

modulo = 55 % 2  #resto da divisão
print('Módulo', modulo)

print(10 % 8 == 0)
print(16 % 8 == 0)
print(10 % 2 == 0)